#include "libft.h"

void	test_printables(void)
{
	char c;
	
	c = ' ';
	while (c <= '~')
	{
		ft_putchar(c);
		ft_putchar('\n');
		c++;
	}
}

int main()
{
	test_printables();
	return (0);
}
