#include "libft.h"

void	test_ft_swap(void)
{
	char	a;
	char	b;

	a = 'a';
	b = 'b';
	ft_swap(&a, &b);
	ft_putchar(a);
	ft_putchar(b);
}

int	main()
{
	test_ft_swap();
	return (0);
}
