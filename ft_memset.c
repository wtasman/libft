#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	size_t	i;

	i = 0;
	b = (unsigned char*)b;
	while (i < len)
	{
		b[i] = c;
		i++;
	}
	ft_putendl(b);
	return (b);
}
